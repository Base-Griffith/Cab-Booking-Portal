    int GiveNum(String Node)
    {
        Node=Node.toLowerCase();
        if(Node.equals("banjara hills"))
            return 0;
        if(Node.equals("gachibowli"))
            return 7;
        if(Node.equals("begumpet"))
            return 1;
        if(Node.equals("ameerpet"))
            return 3;
        if(Node.equals("medchal"))
            return 6;
        if(Node.equals("kukatpally"))
            return 2;
        if(Node.equals("hitec city"))
            return 8;
        if(Node.equals("uppal"))
            return 5;
        if(Node.equals("shamirpet"))
            return 4;
        return -1;
    }
    
    
